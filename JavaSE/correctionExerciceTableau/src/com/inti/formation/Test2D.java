package com.inti.formation;

public class Test2D {

	public static void main(String[] args) {
		int[][] tab = { { 5, 47, 89, 63 }, { 122, 48, 56, 78 } };

		for (int i = 0; i < tab.length; i++) {
			for (int j = 0; j < tab[i].length; j++) {
				System.out.println(tab[i][j]);
			}
		}

	}

}
